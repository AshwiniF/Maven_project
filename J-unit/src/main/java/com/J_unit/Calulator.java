package com.J_unit;

public class Calulator {
 public	int Addition(int a,int b) {
		int c=a+b;
		return c;
	}
 public int Substraction(int a,int b) {
		int c=a-b;
		return c;
	}
 public int Multiplication(int a,int b) {
		int c=a*b;
		return c;
	}
 public int Divison(int a,int b) {
		int c=a/b;
		return c;
	}

}
